  arch arm: "arm64", intel: "x64"

  version "10.0.100-preview.7.25380.108,10.0.0-preview.7.25380.108"

  sha256_x64 = "63e9f6b70ad8f947aa36db959e490c90054d3ee9aff58f73c913b0ca59cd96f6"
